#include "cub3D.h"

unsigned int	get_hex_in_decimal(uint8_t *rgba, int i)
{
	return (rgba[i] << (8 * 3) | rgba[i + 1] << (8 * 2)
		| rgba[i + 2] << 8 | rgba[i + 3]);
}
